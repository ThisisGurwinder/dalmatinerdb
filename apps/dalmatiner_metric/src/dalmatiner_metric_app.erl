-module(dalmatiner_metric_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    ddb_counter:init(),
    ddb_counter:register(mps),
    dalmatiner_metric_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

